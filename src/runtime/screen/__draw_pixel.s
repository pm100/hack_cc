// PROVIDES: __draw_pixel
// DEPS:
// Set pixel (R13=x, R14=y) black. Preserves all registers. Return via R3.
// Screen base 16384. word = 16384 + (y*32) + (x/16). bit = x mod 16.
// Uses R5-R9 as scratch; saves and restores them.
(__draw_pixel)
// Save R5-R9
@R5
D=M
@SP
A=M
M=D
@SP
M=M+1
@R6
D=M
@SP
A=M
M=D
@SP
M=M+1
@R7
D=M
@SP
A=M
M=D
@SP
M=M+1
@R8
D=M
@SP
A=M
M=D
@SP
M=M+1
@R9
D=M
@SP
A=M
M=D
@SP
M=M+1
// y*32 into R5 (5 doublings)
@R14
D=M
@R5
M=D
@R5
D=M
M=D+M
@R5
D=M
M=D+M
@R5
D=M
M=D+M
@R5
D=M
M=D+M
@R5
D=M
M=D+M
// x/16 quotient->R6, remainder->R7
@R13
D=M
@R7
M=D
@R6
M=0
(__dp_div16)
@R7
D=M
@16
D=D-A
@__dp_div16_done
D;JLT
@R7
M=D
@R6
M=M+1
@__dp_div16
0;JMP
(__dp_div16_done)
// bit_mask = 1 << (x%16) into R9
@R9
M=1
(__dp_shift)
@R7
D=M
@__dp_shift_done
D;JEQ
@R9
D=M
M=D+M
@R7
M=M-1
@__dp_shift
0;JMP
(__dp_shift_done)
// screen_word = 16384 + y*32 + x/16
@16384
D=A
@R5
D=D+M
@R6
D=D+M
@R8
M=D
// OR in the bit
@R8
A=M
D=M
@R9
D=D|M
@R8
A=M
M=D
// Restore R9-R5 (reverse order)
@SP
AM=M-1
D=M
@R9
M=D
@SP
AM=M-1
D=M
@R8
M=D
@SP
AM=M-1
D=M
@R7
M=D
@SP
AM=M-1
D=M
@R6
M=D
@SP
AM=M-1
D=M
@R5
M=D
@R3
A=M
0;JMP
